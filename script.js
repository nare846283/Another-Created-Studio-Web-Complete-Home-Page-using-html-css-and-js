const sliderContainer1 = document.querySelector('#sliding-container1');
const leftArrow1 = document.querySelector('#left-arrow1');
const rightArrow1 = document.querySelector('#right-arrow1');

let currentIndex1 = 0;

leftArrow1.addEventListener('click', () => {
    currentIndex1 = (currentIndex1 - 1 < 0) ? 7 : currentIndex1 - 1;
    updateSlider1();
});

rightArrow1.addEventListener('click', () => {
    currentIndex1 = (currentIndex1 + 1 > 7) ? 0 : currentIndex1 + 1;
    updateSlider1();
});

function updateSlider1() {
    const offset = -currentIndex1 * 25 + '%';
    sliderContainer1.style.transform = `translateX(${offset})`;
}


